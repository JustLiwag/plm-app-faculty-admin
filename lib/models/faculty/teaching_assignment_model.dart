class TeachingAssignment {
  final String subjectCodeAndSection;
  final String subjectTitle;
  final String units;
  final String schedule;
  final String noStudents;
  final String creditedUnits;
  final String college;
  final String typeOfLoad;

  TeachingAssignment({
    required this.subjectCodeAndSection,
    required this.subjectTitle,
    required this.units,
    required this.schedule,
    required this.noStudents,
    required this.creditedUnits,
    required this.college,
    required this.typeOfLoad,
  });
}
